#!/usr/bin/env node
// scripts/docs/generate-playbook-index.mjs
//
// Scans all docs/playbooks/*.md files, extracts the title and **Trigger:**
// line, and generates docs/playbooks/INDEX.md — a lookup table for agents
// and contributors.
//
// Usage:
//   node scripts/docs/generate-playbook-index.mjs          # write INDEX.md
//   node scripts/docs/generate-playbook-index.mjs --check  # CI mode: fail if INDEX.md is stale

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = resolve(__dirname, "../..");
const PLAYBOOKS_DIR = resolve(ROOT, "docs/playbooks");
const INDEX_PATH = resolve(PLAYBOOKS_DIR, "INDEX.md");

const CHECK_MODE = process.argv.includes("--check");

// ── Scan playbooks ───────────────────────────────────────────────────────────

const entries = [];

const files = readdirSync(PLAYBOOKS_DIR)
  .filter((f) => f.endsWith(".md") && f !== "README.md" && f !== "INDEX.md")
  .sort();

for (const file of files) {
  const content = readFileSync(resolve(PLAYBOOKS_DIR, file), "utf-8");
  const lines = content.split("\n");

  // Extract title from first # heading
  const titleLine = lines.find((l) => l.startsWith("# "));
  const title = titleLine
    ? titleLine.replace(/^#\s+/, "").replace(/^Playbook:\s*/i, "")
    : file.replace(".md", "");

  // Extract trigger phrase
  const triggerLine = lines.find(
    (l) => l.startsWith("**Trigger:**") || l.startsWith("**Trigger:** "),
  );
  const trigger = triggerLine
    ? triggerLine.replace(/^\*\*Trigger:\*\*\s*/, "").trim()
    : "—";

  entries.push({ file, title, trigger });
}

// ── Generate INDEX.md ────────────────────────────────────────────────────────

const header = `# Playbook Index

> Auto-generated by \`scripts/docs/generate-playbook-index.mjs\`.
> Do not edit manually — run \`pnpm docs:playbook-index\` to regenerate.

> **Last validated:** ${new Date().toISOString().slice(0, 10)} by @devin-ai.
> **Status:** Active

| Playbook | Trigger phrase(s) |
| -------- | ----------------- |
`;

const rows = entries
  .map((e) => `| [${e.title}](${e.file}) | ${e.trigger} |`)
  .join("\n");

const content = header + rows + "\n";

// ── Write or check ───────────────────────────────────────────────────────────

if (CHECK_MODE) {
  let existing = "";
  try {
    existing = readFileSync(INDEX_PATH, "utf-8");
  } catch {
    console.error(
      "❌ docs/playbooks/INDEX.md does not exist. Run: pnpm docs:playbook-index",
    );
    process.exit(1);
  }

  // Compare ignoring the date line (changes daily) and table whitespace
  // (prettier pads table cells, our generator doesn't).
  const normalize = (s) =>
    s
      .split("\n")
      .filter((l) => !l.startsWith("> **Last validated:**"))
      .map((l) =>
        l.startsWith("|")
          ? l.replace(/\s*\|\s*/g, "|").replace(/^(\|[-|]+\|)$/, "|---|---|")
          : l,
      )
      .join("\n");

  if (normalize(existing) !== normalize(content)) {
    console.error(
      "❌ docs/playbooks/INDEX.md is stale. Run: pnpm docs:playbook-index",
    );
    process.exit(1);
  }

  console.log("✅ docs/playbooks/INDEX.md is up to date.");
} else {
  writeFileSync(INDEX_PATH, content);
  console.log(
    `✅ Generated docs/playbooks/INDEX.md with ${entries.length} playbooks.`,
  );
}
